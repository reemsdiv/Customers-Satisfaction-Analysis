# -*- coding: utf-8 -*-
"""
سكريبت تنظيف وتوحيد بيانات استبيان رضا العملاء
------------------------------------------------
ملاحظة مهمة: هذا السكريبت لا يحذف أي صف أو عمود من البيانات الأصلية.
كل ما يفعله هو: توحيد التنسيقات، تصحيح أسماء الأعمدة لتكون أوضح،
وتوحيد القيم المتشابهة (مثل "لايوجد" و"لا يوجد" و".") لتصبح قيمة واحدة موحدة.
"""

import pandas as pd
import numpy as np
import re

# =========================================================
# 1) إعدادات عامة: مسار الملف المدخل والملف الناتج
# =========================================================
INPUT_FILE = "data_labeled.xlsx"
OUTPUT_FILE = "data_cleaned.xlsx"

# قراءة الشيت كما هو (بدون أي تعديل على البيانات الخام بعد)
df = pd.read_excel(INPUT_FILE)

print("عدد الصفوف قبل التنظيف:", len(df))
print("عدد الأعمدة قبل التنظيف:", len(df.columns))

# =========================================================
# 2) توحيد أسماء الأعمدة لتكون أوضح وأسهل في التعامل
#    (رموز مثل Q1, Q3b, Q8.1 تُستبدل بأسماء عربية واضحة)
#    ملاحظة: بعض الأعمدة (مثل Q2, Q3, Q3b...) أسئلة تقييم فرعية
#    لا نعرف نص السؤال الأصلي لها بدقة، لذلك تم إعطاؤها أسماء
#    مرقّمة واضحة بدل ترك الرمز الأصلي فقط.
# =========================================================
rename_map = {
    "responseId": "رقم_الاستجابة",
    "DATE": "تاريخ_الاستجابة",
    "IS": "وقت_البداية",
    "IE": "وقت_النهاية",
    "IN": "اسم_الباحث",
    "F1": "اسم_الخدمة",

    "Q1": "تقييم_1",
    "Q1A": "تعليق_تقييم_1",
    "primary_reason_Sat": "السبب_الرئيسي_للرضا",
    "secondary_reason_Sat": "السبب_الثانوي_للرضا",

    "Q2": "تقييم_2",
    "Q2A": "تعليق_تقييم_2",

    "Q3": "تقييم_3",
    "Q3.1": "تعليق_تقييم_3",
    "Q3b": "تقييم_3ب",
    "Q3b.1": "تعليق_تقييم_3ب",
    "Q3c": "تقييم_3ج",
    "Q3c.1": "تعليق_تقييم_3ج",
    "Q3d": "تقييم_3د",
    "Q3d.1": "تعليق_تقييم_3د",

    "Q5b": "تقييم_5ب",
    "Q5b.1": "تعليق_تقييم_5ب",
    "Q5c": "تقييم_5ج",
    "Q5c.1": "تعليق_تقييم_5ج",

    "Q8": "تقييم_الترشيح_NPS",
    "Q8.1": "تعليق_الترشيح",
    "primary_reason_Rec": "السبب_الرئيسي_للترشيح",
    "secondary_reason_Rec": "السبب_الثانوي_للترشيح",

    # عمود AA بقي بدون إعادة تسمية لأن معناه غير واضح من البيانات
    # (قيمه فقط 1 أو 2)، يفضل التأكد من معناه مع صاحب الاستبيان الأصلي
}

df = df.rename(columns=rename_map)

# =========================================================
# 3) توحيد التاريخ والوقت وحساب مدة تعبئة الاستبيان
# =========================================================
# تحويل عمود التاريخ إلى صيغة تاريخ موحدة (datetime)
df["تاريخ_الاستجابة"] = pd.to_datetime(df["تاريخ_الاستجابة"], errors="coerce")

# دمج التاريخ مع وقت البداية والنهاية للحصول على وقت كامل (تاريخ + وقت)
تاريخ_نصي = df["تاريخ_الاستجابة"].dt.strftime("%Y-%m-%d")

df["وقت_البداية_الكامل"] = pd.to_datetime(
    تاريخ_نصي + " " + df["وقت_البداية"].astype(str),
    format="%Y-%m-%d %I:%M %p", errors="coerce"
)
df["وقت_النهاية_الكامل"] = pd.to_datetime(
    تاريخ_نصي + " " + df["وقت_النهاية"].astype(str),
    format="%Y-%m-%d %I:%M %p", errors="coerce"
)

# حساب مدة تعبئة الاستبيان بالدقائق (عمود جديد مفيد للتحليل لاحقاً)
df["مدة_الاستبيان_بالدقائق"] = (
    df["وقت_النهاية_الكامل"] - df["وقت_البداية_الكامل"]
).dt.total_seconds() / 60

# =========================================================
# 4) توحيد اسم الباحث واسم الخدمة (إزالة المسافات الزائدة فقط)
#    ملاحظة: بعض الأسماء كانت مكررة بسبب مسافة زائدة في النهاية
#    مثل "رحاب" و"رحاب " -> أصبحت الآن نفس القيمة تماماً
# =========================================================
df["اسم_الباحث"] = df["اسم_الباحث"].astype(str).str.strip()
df.loc[df["اسم_الباحث"].isin(["nan", "None", ""]), "اسم_الباحث"] = np.nan

df["اسم_الخدمة"] = df["اسم_الخدمة"].astype(str).str.strip()
df.loc[df["اسم_الخدمة"].isin(["nan", "None", ""]), "اسم_الخدمة"] = np.nan

# =========================================================
# 5) توحيد أعمدة التقييمات الرقمية
#    القيمة 99 تعني "غير منطبق" حسب تصميم الاستبيان الأصلي، وهي
#    مب تقييم حقيقي. لو تُركت 99 بتخرب أي متوسط (AVERAGE) نحسبه
#    لاحقاً لأنها بترفع الرقم بشكل غير صحيح.
#    الحل: تتحول مباشرة لقيمة فاضية (NaN)، وبذا أي حساب متوسط أو
#    تحليل بيتجاهلها تلقائياً من نفسه بدون الحاجة لعمود إضافي.
# =========================================================
rating_cols = [
    "تقييم_1", "تقييم_2", "تقييم_3", "تقييم_3ب", "تقييم_3ج",
    "تقييم_3د", "تقييم_5ب", "تقييم_5ج", "تقييم_الترشيح_NPS",
]

for col in rating_cols:
    df[col] = pd.to_numeric(df[col], errors="coerce")
    df.loc[df[col] == 99, col] = np.nan  # 99 (غير منطبق) تصير فاضية بدل تقييم فعلي

# =========================================================
# 6) توحيد أعمدة التعليقات النصية (أسباب التقييم المكتوبة)
#    - إزالة علامات الاقتباس الزائدة من الأطراف
#    - توحيد المسافات المتكررة إلى مسافة واحدة
#    - توحيد كل صيغ "لا يوجد تعليق" (.، لايوجد، لابوجد، لا يوجد...)
#      لتصبح نصاً واحداً موحداً بدل عدة أشكال مختلفة لنفس المعنى
# =========================================================
comment_cols = [
    "تعليق_تقييم_1", "تعليق_تقييم_2", "تعليق_تقييم_3",
    "تعليق_تقييم_3ب", "تعليق_تقييم_3ج", "تعليق_تقييم_3د",
    "تعليق_تقييم_5ب", "تعليق_تقييم_5ج", "تعليق_الترشيح",
]

# كل الصيغ المختلفة التي تعني "لا يوجد تعليق" بعد إزالة المسافات الزائدة
صيغ_لا_يوجد_تعليق = {".", "لايوجد", "لابوجد", "لا يوجد", "لا يوجد.", "لايوجد."}


def توحيد_نص_التعليق(نص):
    """تنظف نص التعليق وتوحّد صيغ (لا يوجد تعليق) دون حذف أي تعليق حقيقي."""
    if pd.isna(نص):
        return نص

    نص_منظف = str(نص).strip()
    نص_منظف = نص_منظف.strip('"').strip()   # إزالة علامات اقتباس زائدة بالأطراف فقط
    نص_منظف = re.sub(r"\s+", " ", نص_منظف)  # توحيد المسافات المتكررة إلى مسافة واحدة

    if نص_منظف in صيغ_لا_يوجد_تعليق:
        return "لا يوجد تعليق"

    return نص_منظف


for col in comment_cols:
    df[col] = df[col].apply(توحيد_نص_التعليق)

# =========================================================
# 7) توحيد أعمدة تصنيف السبب (فئات جاهزة أصلاً، فقط تنظيف مسافات)
# =========================================================
reason_cols = [
    "السبب_الرئيسي_للرضا", "السبب_الثانوي_للرضا",
    "السبب_الرئيسي_للترشيح", "السبب_الثانوي_للترشيح",
]

for col in reason_cols:
    df[col] = df[col].astype(str).str.strip()
    df.loc[df[col].isin(["nan", "None", ""]), col] = np.nan

# =========================================================
# 8) حفظ الملف النهائي (نفس عدد الصفوف والأعمدة الأصلية بالكامل)
# =========================================================
df.to_excel(OUTPUT_FILE, index=False)

print("عدد الصفوف بعد التنظيف:", len(df))
print("عدد الأعمدة بعد التنظيف:", len(df.columns))
print("تم حفظ الملف النظيف في:", OUTPUT_FILE)